function getCharBihua(ch) {
  if (KANGXI_BIHUA[ch] !== undefined) return KANGXI_BIHUA[ch];
  return fallbackBihua(ch);
}

function fallbackBihua(ch) {
  // 康熙字典未收录时，提示用户并使用近似估算
  // 根据汉字部首复杂度给出更合理的默认笔画
  let code = ch.charCodeAt(0);
  if (code < 0x4E00 || code > 0x9FFF) return 8;
  // 简体/常用字 8-15 画，繁体/罕见字 15-25 画
  // 用 Unicode 区段做粗略分类（CJK 基本区 0x4E00-0x9FFF）
  const offset = code - 0x4E00;
  // 经验区间：1-10000 字（最常用）→ 5-13 画
  //           10000-30000 字（次常用）→ 12-18 画
  //           30000+ 字（罕见）→ 18-28 画
  if (offset < 10000) {
    return Math.min(13, Math.max(5, Math.floor(offset / 700) + 5));
  } else if (offset < 30000) {
    return Math.min(18, Math.max(12, Math.floor((offset - 10000) / 1100) + 12));
  } else {
    return Math.min(28, Math.max(18, Math.floor((offset - 30000) / 1500) + 18));
  }
}

function calculateGege(surname, name) {
  const x = Array.from(surname);
  const m = Array.from(name);
  const xing = x[0] || '';
  const xingLast = x[x.length - 1] || xing;  // 复姓取末字，单姓取首字
  const ming1 = m[0] || '';
  const ming2 = m[1] || '';

  const xingBi = getCharBihua(xing);
  const xingLastBi = getCharBihua(xingLast);
  const ming1Bi = ming1 ? getCharBihua(ming1) : 0;
  const ming2Bi = ming2 ? getCharBihua(ming2) : 0;

  let tiange, renge, dige, waige, zongge;

  if (x.length === 1) {
    // 单姓
    tiange = xingBi + 1;
  } else {
    // 复姓
    tiange = xingBi + getCharBihua(x[1]);
  }

  if (ming2) {
    // 双名
    renge = xingLastBi + ming1Bi;  // 人格 = 姓末 + 名首
    dige = ming1Bi + ming2Bi;
    zongge = xingBi + (x.length > 1 ? getCharBihua(x[1]) : 0) + ming1Bi + ming2Bi;
    waige = zongge - renge + 1;
  } else {
    // 单名
    renge = xingLastBi + ming1Bi;  // 人格 = 姓末 + 名首
    dige = ming1Bi + 1;
    zongge = xingBi + (x.length > 1 ? getCharBihua(x[1]) : 0) + ming1Bi;
    waige = zongge - renge + 1;
  }

  return {
    surname: x.join(''),
    name: m.join(''),
    xingBi, ming1Bi, ming2Bi,
    tiange, renge, dige, waige, zongge,
    sancaiWuge: [
      WUXING_BIHUA[tiange],
      WUXING_BIHUA[renge],
      WUXING_BIHUA[dige]
    ]
  };
}

function buildXingshiPrompt(data, gender, birth) {
  let s = '=== 姓名学五格剖象 ===\n';
  s += `姓名：${data.surname}${data.name}\n`;
  s += `性别：${gender === 'male' ? '男' : '女'}\n`;
  if (birth) s += `生辰：${birth}\n`;
  s += '\n笔画明细：\n';
  s += `  姓"${data.surname}"：${data.surname.split('').map(c => `${c}(${getCharBihua(c)}画)`).join('、')}\n`;
  if (data.ming1Bi) {
    s += `  名"${data.name}"：${data.name.split('').map(c => `${c}(${getCharBihua(c)}画)`).join('、')}\n`;
  }
  s += '\n五格：\n';
  s += `  天格${data.tiange}（${WUXING_BIHUA[data.tiange]}）：先天运、父母运\n`;
  s += `  人格${data.renge}（${WUXING_BIHUA[data.renge]}）：主运、性格、事业【最重要】\n`;
  s += `  地格${data.dige}（${WUXING_BIHUA[data.dige]}）：前运、基础运、子女运\n`;
  s += `  外格${data.waige}（${WUXING_BIHUA[data.waige]}）：副运、社交、人际\n`;
  s += `  总格${data.zongge}（${WUXING_BIHUA[data.zongge]}）：后运、晚年、人生总格局\n`;
  s += `\n三才配置：${data.sancaiWuge.join(' → ')}\n`;
  s += '\n请结合以上五格与三才进行专业解读。';
  return s;
}

